for i in range(6):  # 電腦會由 0 ~ 5 的數字
    print('正在執行:', i)
print('Program end.')

print('-'*10)

for i in range(1, 6):  # 可設定從哪一數字開始執行
    print('正在執行:', i)
print('Program end.')

print('-'*10)

for i in range(1, 6+1):  # 可設定從哪一數字開始執行 如要做到數字x 後方需+1
    print('正在執行:', i)
print('Program end.')

print('-'*10)

for i in range(1, 6+1, 1):  # 可設定從哪一數字開始執行 如需要有間格的數字 (從哪開始, 幾圈 , 間格數字)
    print('正在執行:', i)
print('Program end.')

print('-'*10)

for i in range(1, 6+1, 1):  # 可設定從哪一數字開始執行 如需要有間格的數字 (從哪開始, 幾圈 , 間格數字)
    print('正在執行:', i)
print('Program end.')

print('-'*10)

for i in range(6, 0, -1):  # 反向  可設定從哪一數字開始執行 如需要有間格的數字 (從哪開始, 幾圈 , 間格數字)
    print('正在執行:', i)
print('Program end.')
